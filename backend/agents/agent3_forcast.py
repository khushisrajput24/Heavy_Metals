class MultiHeadTemporalAttention(nn.Module):

    def __init__(self, embed_dim):

        super().__init__()

        self.attn = nn.MultiheadAttention(

            embed_dim=embed_dim,

            num_heads=4,

            batch_first=True,

            dropout=0.1

        )

        self.norm = nn.LayerNorm(embed_dim)

    def forward(self, x):

        attn_output, _ = self.attn(

            x,

            x,

            x

        )

        x = self.norm(

            x + attn_output

        )

        return x.mean(dim=1)

class GroundwaterBiLSTM(nn.Module):

    def __init__(self):

        super().__init__()

        input_size = len(INPUT_COLUMNS)

        ########################################

        self.lstm1 = nn.LSTM(
            input_size=input_size,
            hidden_size=64, # Changed from 96 to 64
            batch_first=True,
            bidirectional=True
        )

        self.norm1 = nn.LayerNorm(128)

        self.dropout1 = nn.Dropout(0.15)

        ########################################

        self.lstm2 = nn.LSTM(
            input_size=128,
            hidden_size=32, # Changed from 48 to 32
            batch_first=True,
            bidirectional=True
        )

        self.norm2 = nn.LayerNorm(64)

        self.dropout2 = nn.Dropout(0.15)

        ########################################

        # Assuming 'Attention' refers to 'MultiHeadTemporalAttention'
        self.attention = MultiHeadTemporalAttention(64) # Changed from Attention(32) to MultiHeadTemporalAttention(64)

        ########################################

        self.fc1 = nn.Linear(64,64)

        self.fc2 = nn.Linear(64,64)

        self.norm = nn.LayerNorm(64)

        self.dropout = nn.Dropout(0.15)

        ########################################
        # Heavy Metal Head
        ########################################

        self.metal_head = nn.Sequential(

            nn.Linear(64,32),

            nn.ReLU(),

            nn.Linear(32,11)

        )

        ########################################
        # HMPI Head
        ########################################

        self.hmpi_head = nn.Sequential(

            nn.Linear(64,16),

            nn.ReLU(),

            nn.Linear(16,1)

        )

    def forward(self,x):

        x,_ = self.lstm1(x)

        x = self.norm1(x)

        x = self.dropout1(x)

        x,_ = self.lstm2(x)

        x = self.norm2(x)

        x = self.dropout2(x)

        x = self.attention(x)

        identity = x
        x = torch.relu(

    self.fc1(x)

)
        x = self.dropout(x)
        x = self.fc2(x)
        x = self.norm(

    x + identity

)
        metals = self.metal_head(x)
        hmpi = self.hmpi_head(x)

        return torch.cat(

            [

                metals,

                hmpi

            ],

            dim=1

        )

