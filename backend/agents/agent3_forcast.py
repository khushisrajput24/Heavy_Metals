for epoch in range(EPOCHS):

    ######################################

    model.train()

    running_loss = 0

    optimizer.zero_grad()

    ######################################

    for step, (X_batch, y_batch) in enumerate(train_loader):

        X_batch = X_batch.to(device)

        y_batch = y_batch.to(device)

        ##################################

        with torch.cuda.amp.autocast(
            enabled=torch.cuda.is_available()
        ):

            predictions = model(X_batch)

            metal_pred = predictions[:, :11]
            hmpi_pred = predictions[:, 11:]
            metal_true = y_batch[:, :11]
            hmpi_true = y_batch[:, 11:]
            loss_metals = criterion(
                metal_pred,
                metal_true
)
            loss_hmpi = criterion(
    hmpi_pred,
    hmpi_true
)
            loss = 0.6 * loss_metals + 0.4 * loss_hmpi
            loss = loss / ACCUMULATION_STEPS

        ##################################

        scaler.scale(loss).backward()

        ##################################

        if (

            (step + 1) % ACCUMULATION_STEPS == 0

            or

            (step + 1) == len(train_loader)

        ):

            scaler.unscale_(optimizer)

            torch.nn.utils.clip_grad_norm_(

                model.parameters(),

                max_norm=1.0

            )

            scaler.step(optimizer)

            scaler.update()

            optimizer.zero_grad()

        ##################################

        running_loss += (

            loss.item()

            * ACCUMULATION_STEPS

        )

    ######################################

    train_loss = running_loss / len(train_loader)

    train_losses.append(train_loss)

    ######################################
    # Validation
    ######################################

    model.eval()

    running_val = 0

    with torch.no_grad():

        for X_batch, y_batch in val_loader:

            X_batch = X_batch.to(device)

            y_batch = y_batch.to(device)

            predictions = model(X_batch)

            metal_pred = predictions[:, :11]
            hmpi_pred = predictions[:, 11:]
            metal_true = y_batch[:, :11]
            hmpi_true = y_batch[:, 11:]
            loss_metals = criterion(
    metal_pred,
    metal_true
)
            loss_hmpi = criterion(
    hmpi_pred,
    hmpi_true
)
            loss = 0.7 * loss_metals + 0.3 * loss_hmpi
            loss = loss / ACCUMULATION_STEPS

            running_val += loss.item()

    ######################################

    val_loss = running_val / len(val_loader)

    val_losses.append(val_loss)

    ######################################

    scheduler.step(val_loss)

    ######################################

    print(

        f"Epoch {epoch+1}/{EPOCHS}"

        f" | Train:{train_loss:.5f}"

        f" | Validation:{val_loss:.5f}"

    )

    ######################################
    # Save Best Model
    ######################################

    if val_loss < best_loss:

        best_loss = val_loss

        counter = 0

        torch.save(

            {

                "epoch": epoch,

                "model_state_dict": model.state_dict(),

                "optimizer_state_dict": optimizer.state_dict(),

                "validation_loss": val_loss

            },

            "best_groundwater_model.pth"

        )

    else:

        counter += 1

        if counter >= patience:

            print()

            print("Early Stopping Triggered")

            break

