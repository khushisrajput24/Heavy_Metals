    # --------------------------------
    # 2. Pollutant-specific retrieval
    # --------------------------------

    pollutants = extract_pollutants(
        row["Major_Pollutants"]
    )

    pollutant_results = []

    for pollutant in pollutants:

        results = (
            retrieve_pollutant_evidence(
                pollutant,
                row,
                top_k=4
            )
        )

        pollutant_results.extend(
            results
        )


    # --------------------------------
    # 3. Combine results
    # --------------------------------

    all_results = (
        balanced_results
        +
        pollutant_results
    )


    # --------------------------------
    # 4. Remove duplicate evidence
    # --------------------------------

    final_results = (
        deduplicate_results(
            all_results
        )
    )


    return final_results

