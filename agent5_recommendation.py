def retrieve_balanced_policy_context(
    query,
    per_source_k=4
):

    # Retrieve a larger candidate pool
    candidate_k = min(
        100,
        faiss_index.ntotal
    )

    query_embedding = embedding_model.encode(
        [query],
        convert_to_numpy=True,
        normalize_embeddings=True
    )

    query_embedding = np.asarray(
        query_embedding,
        dtype="float32"
    )

    scores, indices = faiss_index.search(
        query_embedding,
        candidate_k
    )

    # Store candidates by source
    source_results = {
        "WHO": [],
        "BIS": [],
        "CGWB": []
    }

    for score, idx in zip(
        scores[0],
        indices[0]
    ):

        if idx == -1:
            continue

        result = knowledge_chunks[idx].copy()

        result["similarity_score"] = float(score)

        source = result["source_type"]

        if source in source_results:

            if len(source_results[source]) < per_source_k:

                source_results[source].append(
                    result
                )

    # Combine results
    final_results = []

    for source in [
        "BIS",
        "WHO",
        "CGWB"
    ]:

        final_results.extend(
            source_results[source]
        )

    return final_results

