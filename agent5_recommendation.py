def generate_local_policy_recommendation(
    row,
    rag_context,
    max_new_tokens=1800
):

    user_prompt = create_local_llm_prompt(
        row,
        rag_context
    )


    messages = [

        {
            "role": "system",
            "content": AGENT5_SYSTEM_PROMPT
        },

        {
            "role": "user",
            "content": user_prompt
        }

    ]


    text = tokenizer.apply_chat_template(

        messages,

        tokenize=False,

        add_generation_prompt=True

    )


    model_inputs = tokenizer(

        [text],

        return_tensors="pt"

    ).to(
        llm_model.device
    )


    with torch.no_grad():

        generated_ids = llm_model.generate(

            **model_inputs,

            max_new_tokens=max_new_tokens,

            do_sample=False,

            repetition_penalty=1.05

        )


    # Remove input prompt tokens
    generated_ids = [

        output_ids[
            len(input_ids):
        ]

        for input_ids, output_ids in zip(

            model_inputs.input_ids,

            generated_ids

        )

    ]


    response = tokenizer.batch_decode(

        generated_ids,

        skip_special_tokens=True

    )[0]


    return response.strip()

