def select_llm_evidence(
    evidence_results,
    max_per_source=3,
    max_total=10
):

    selected = []

    # -----------------------------
    # Ensure source diversity
    # -----------------------------

    for source in [
        "BIS",
        "WHO",
        "CGWB"
    ]:

        source_results = [

            result

            for result in evidence_results

            if result["source_type"] == source

        ]


        source_results = sorted(

            source_results,

            key=lambda x: x[
                "similarity_score"
            ],

            reverse=True

        )


        selected.extend(

            source_results[
                :max_per_source
            ]

        )


    # -----------------------------
    # Deduplicate
    # -----------------------------

    selected = deduplicate_results(
        selected
    )


    # -----------------------------
    # Sort by relevance
    # -----------------------------

    selected = sorted(

        selected,

        key=lambda x: x[
            "similarity_score"
        ],

        reverse=True

    )


    return selected[
        :max_total
    ]

