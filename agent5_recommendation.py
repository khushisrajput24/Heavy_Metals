def deduplicate_results(
    results
):

    seen = set()

    unique_results = []

    for result in results:

        key = (
            result["source"],
            result["page"],
            result["text"]
        )

        if key not in seen:

            seen.add(
                key
            )

            unique_results.append(
                result
            )

    return unique_results

